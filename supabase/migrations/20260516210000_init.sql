  started_at    timestamptz not null default now(),
  finished_at   timestamptz,
  rows_written  int,
  status        text not null check (status in ('success','partial','error','running')),
  error_message text,
  gha_run_url   text
);
create index ingest_runs_workflow_started_idx on public.ingest_runs (workflow, started_at desc);

-- ─── Daily facts ────────────────────────────────────────────────────────
create table public.plugin_daily_metrics (
  id                bigint generated by default as identity primary key,
  plugin_id         bigint not null references public.plugins(id) on delete cascade,
  observed_on       date not null,
  npm_downloads_d7  int,
  npm_downloads_d30 int,
  npm_version       text,
  github_stars      int,
  github_forks      int,
  rule_count        int,
  published         boolean default true,
  ingest_run_id     uuid references public.ingest_runs(id) on delete set null,
  unique (plugin_id, observed_on)
