-- ─── Generic EAV for everything else ────────────────────────────────────
create table public.metric_snapshots (
  id            bigint generated by default as identity primary key,
  source        text not null,
  kind          text not null,
  dimension     text not null default '',
  observed_on   date not null,
  value         numeric(20,4),
  payload       jsonb,
  ingest_run_id uuid references public.ingest_runs(id) on delete set null,
  unique (source, kind, dimension, observed_on)
);
create index metric_snapshots_source_kind_idx on public.metric_snapshots (source, kind, observed_on desc);
create index metric_snapshots_observed_idx on public.metric_snapshots (observed_on desc);

-- ─── Storefront ratchet (the North Star) ────────────────────────────────
create table public.storefront_ratchet (
  kind           text primary key,
  bucket         text not null check (bucket in ('contributions','engagement','north_star')),
  current_value  bigint not null default 0,
  updated_at     timestamptz not null default now(),
  source_kinds   jsonb not null default '{}'::jsonb,
  display_label  text not null,
  display_unit   text,
  display_icon   text,
  display_order  int not null default 0,
  description    text,
  provenance_url text,
  weight         numeric(8,4) not null default 1.0
);

-- GREATEST trigger: current_value is monotonically non-decreasing at the row level.
create or replace function public.enforce_ratchet_monotonic()
returns trigger language plpgsql as $$
begin
  if TG_OP = 'UPDATE' then
    new.current_value := greatest(old.current_value, new.current_value);
  end if;
  new.updated_at := now();
  return new;
end;
$$;

create trigger storefront_ratchet_monotonic
before insert or update on public.storefront_ratchet
for each row execute function public.enforce_ratchet_monotonic();

