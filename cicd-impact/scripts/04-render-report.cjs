#!/usr/bin/env node
// Render outputs/unit-cost.json + outputs/sensitivity.csv into a markdown
// fragment (outputs/presentation-table.md) the README pulls in.
//
// Also produces outputs/headline.md — the single number, formatted for slides.
//
// Usage: ./04-render-report.cjs [--outdir path]
const fs = require('node:fs');
const path = require('node:path');
const realFmt = require('./lib/format.cjs');

const args = process.argv.slice(2);
const arg = (name, fallback) => {
  const i = args.indexOf(name);
  return i >= 0 ? args[i + 1] : fallback;
};

const root = path.join(__dirname, '..');
const outDir = arg('--outdir', path.join(root, 'outputs'));
const unitCost = JSON.parse(fs.readFileSync(path.join(outDir, 'unit-cost.json'), 'utf8'));
const sensCsv = fs.readFileSync(path.join(outDir, 'sensitivity.csv'), 'utf8').trim().split('\n');

const h = unitCost.headline;
const d = unitCost.decomposition;
const i = unitCost.inputs_resolved;
const c = unitCost.context;

// --- Headline ----------------------------------------------------------------
const headlineMd = `**${realFmt.usd(h.cost_per_ci_minute_usd)} per CI/CD minute** \
(${realFmt.usd(h.cost_per_ci_second_usd, { precision: 4 })}/second). \
Annualised: **${realFmt.usd(h.annual_cost_usd, { compact: true })}**.

This is the cost the engineering team pays the company for each minute that the \
CI/CD pipeline takes to run, accounting for direct idle time, focus-loss, and \
spillover when builds fail. It is a *unit cost*: every minute saved (or added) \
multiplies it by the number of pipeline-minutes-per-year.

Annual pipeline-minutes (the multiplier): **${realFmt.number(c.annual_pipeline_minutes)}** \
= ${realFmt.minutes(i.T_pipeline_min)}/run × ${i.R_runs_per_business_day.toFixed(1)} runs/biz-day × ${i.business_days_per_year} biz-days × ${i.D_active_developers} devs.
`;

fs.writeFileSync(path.join(outDir, 'headline.md'), headlineMd);

// --- Decomposition table -----------------------------------------------------
const decompMd = `
| Bucket | Annual cost | Multiplier | What this is |
|---|---:|:---:|---|
| Direct waste (idle time) | ${realFmt.usd(d.annual_direct, { compact: true })} | 1.00× | Developer paid wage × pipeline minutes × runs × devs. The "watching the progress bar" cost. |
| Cognitive tax | ${realFmt.usd(d.annual_cognitive, { compact: true })} | ×${(d.cognitive_multiplier - 1).toFixed(2)} | Focus-loss when the pipeline crosses the ${i.S_band === 'long' ? '10-min' : '2-min'} threshold (${i.S_cognitive_min}-min recovery applied per Mark/Gloria/Klocke 2008, piecewise). |
| Failure rework | ${realFmt.usd(d.annual_rework, { compact: true })} | ×${(d.rework_multiplier - 1).toFixed(2)} | Failed builds × blast-radius (one developer's broken STG blocks K others). |
| **Total** | **${realFmt.usd(h.annual_cost_usd, { compact: true })}** | — | |
`.trim() + '\n';

// --- Sensitivity (collapse to a slide-friendly Low/Med/High) -----------------
// Pick the rows that pin F to its measured value (null override) and span
// wage_multiplier × blast_radius_multiplier.
const sensRows = sensCsv.slice(1).map((line) => line.split(','));
const measuredRows = sensRows.filter((r) => Math.abs(parseFloat(r[3]) - i.F_failure_rate) < 0.0005);

let sensMd = '\n| Wage / min | Blast radius (K) | $/CI min | Annual |\n|---:|:---:|---:|---:|\n';
for (const r of measuredRows) {
  sensMd += `| ${realFmt.usd(parseFloat(r[1]))} | ${parseFloat(r[2]).toFixed(2)} | ${realFmt.usd(parseFloat(r[4]))} | ${realFmt.usd(parseInt(r[5], 10), { compact: true })} |\n`;
}

// --- Combined presentation file ---------------------------------------------
const presentationMd = `<!-- generated by cicd-impact/scripts/04-render-report.cjs — do not edit -->
## Headline

${headlineMd}

## Decomposition

${decompMd}

## Sensitivity (failure rate held at measured ${realFmt.percent(i.F_failure_rate)})

${sensMd}

## Resolved inputs

- **W** (wage / dev-minute, fully loaded): ${realFmt.usd(i.W_wage_per_min_usd)}
- **D** (active developers, last ${c.metrics_window.days} d): ${i.D_active_developers} *(${i.D_source})*
- **R** (CI runs / business day): ${i.R_runs_per_business_day.toFixed(1)}
- **T_dur** (mean run wall-clock): ${realFmt.minutes(i.T_dur_mean_min)}
- **T_queue** (mean per-run queue): ${realFmt.minutes(i.T_queue_mean_min)}
- **T_pipeline** (T_dur + T_queue): ${realFmt.minutes(i.T_pipeline_min)}
- **F** (CI failure rate, policy-adjusted): ${realFmt.percent(i.F_failure_rate)}
- **K** (blast-radius multiplier): ${i.K_blast_radius.toFixed(2)} *(${i.K_source})*
- **S** (cognitive-tax minutes, piecewise): ${i.S_cognitive_min} min for the **${i.S_band}** band

## Window

- ${c.metrics_window.days} days (${c.metrics_window.business_days} business days)
- ${c.metrics_window.runs_total} CI/CD runs (${c.metrics_window.runs_completed} completed)
- ${c.metrics_window.jobs_total} jobs analysed
- Generated: ${unitCost.generated_at}
`;

fs.writeFileSync(path.join(outDir, 'presentation-table.md'), presentationMd);

console.log(`Wrote ${path.join(outDir, 'headline.md')}`);
console.log(`Wrote ${path.join(outDir, 'presentation-table.md')}`);
console.log();
console.log('Headline:');
console.log(headlineMd);
